class ProductModel {
  final String img;
  final String id;
  final String nombre;
  final String precio;
  final String modelo;
  final String marca;
  final String color;
  final String region;
  final String categoria;

  ProductModel({
    required this.img,
    required this.id,
    required this.nombre,
    required this.precio,
    required this.modelo,
    required this.marca,
    required this.color,
    required this.region,
    required this.categoria,
  });
}
